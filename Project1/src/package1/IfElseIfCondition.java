package package1;

public class IfElseIfCondition {

	public static void main(String[] args) {
		int a=9;
		int b=19;
		
		if(a<b) 
		{
			System.out.println("a is smaller than b");
		}
		else if(a==b) 
		{
			System.out.println("a is equal to b");
		}
		else 
		{
			System.out.println("a is greater than b");
		}
		
	}

}
